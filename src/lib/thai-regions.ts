/**
 * Resolve a province string to its region.
 * - Trims whitespace + strips leading "จ." / "จังหวัด" prefix that DB may have.
 * - Returns "อื่นๆ" if unknown — never throws, never returns null.
 */
export function getRegion(location: string | null | undefined): ThaiRegion {
  if (!location) return "อื่นๆ"
  const cleaned = location.trim().replace(/^(จังหวัด|จ\.)\s*/u, "").trim()
  return PROVINCE_TO_REGION[cleaned] ?? "อื่นๆ"
}
